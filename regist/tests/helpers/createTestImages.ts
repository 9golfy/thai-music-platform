// Helper to create tiny test images programmatically
import { writeFileSync } from 'fs';
import { join } from 'path';

export function createTestImages() {
  const fixturesDir = join(process.cwd(), 'tests', 'fixtures');
  
  // Create a minimal valid PNG (1x1 pixel, red)
  // PNG signature + IHDR + IDAT + IEND
  const pngData1 = Buffer.from([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
    0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52, // IHDR chunk
    0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, // 1x1 dimensions
    0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53,
    0xDE, 0x00, 0x00, 0x00, 0x0C, 0x49, 0x44, 0x41, // IDAT chunk
    0x54, 0x08, 0xD7, 0x63, 0xF8, 0xCF, 0xC0, 0x00,
    0x00, 0x03, 0x01, 0x01, 0x00, 0x18, 0xDD, 0x8D,
    0xB4, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, // IEND chunk
    0x44, 0xAE, 0x42, 0x60, 0x82
  ]);

  // Create a minimal valid PNG (1x1 pixel, blue)
  const pngData2 = Buffer.from([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,
    0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52,
    0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
    0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53,
    0xDE, 0x00, 0x00, 0x00, 0x0C, 0x49, 0x44, 0x41,
    0x54, 0x08, 0xD7, 0x63, 0x60, 0x00, 0xF8, 0x0F,
    0x00, 0x01, 0x04, 0x01, 0x00, 0xDB, 0x8F, 0x58,
    0x7D, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E,
    0x44, 0xAE, 0x42, 0x60, 0x82
  ]);

  writeFileSync(join(fixturesDir, 'photo1.png'), pngData1);
  writeFileSync(join(fixturesDir, 'photo2.png'), pngData2);
  
  console.log('Test images created successfully');
}

// Run if executed directly
if (require.main === module) {
  createTestImages();
}
